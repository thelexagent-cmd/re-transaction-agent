@router.get("/portal/lender/{token}")
async def get_lender_portal_view(
    token: str,
    db: AsyncSession = Depends(get_db),
) -> dict:
    """Public endpoint — return lender portal data for a valid lender token.

    No authentication required. The token itself is the credential.
    Returns transaction summary + document list for uploading.
    """
    now = datetime.now(timezone.utc)

    result = await db.execute(
        select(PortalToken)
        .where(
            PortalToken.token == token,
            PortalToken.token_type == "lender",
            PortalToken.expires_at > now,
        )
        .options(
            selectinload(PortalToken.transaction).selectinload(Transaction.documents),
        )
    )
    portal_token = result.scalar_one_or_none()
    if portal_token is None:
        raise HTTPException(
            status_code=status.HTTP_404_NOT_FOUND,
            detail="Lender portal link is invalid or has expired.",
        )

    txn = portal_token.transaction

    # Filter documents relevant to lender
    uploaded_docs = [
        {
            "id": doc.id,
            "name": doc.name,
            "status": doc.status,
            "uploaded_at": doc.collected_at.isoformat() if doc.collected_at else doc.created_at.isoformat(),
        }
        for doc in txn.documents
    ]

    return {
        "transaction": {
            "id": txn.id,
            "address": txn.address,
            "status": txn.status,
            "closing_date": txn.closing_date.isoformat() if txn.closing_date else None,
        },
        "lender_name": portal_token.lender_name,
        "required_docs": [
            "Commitment Letter",
            "Clear to Close",
            "Final Loan Approval",
            "Closing Disclosure",
        ],
        "uploaded_docs": uploaded_docs,
    }


@router.post("/portal/lender/{token}/upload", status_code=status.HTTP_201_CREATED)
async def lender_upload_document(
    token: str,
    body: LenderDocUpload,
    db: AsyncSession = Depends(get_db),
) -> dict:
    """Public endpoint — allow lender to submit a document record via the portal.

    Stores document metadata. The frontend handles actual file upload to R2.
    No authentication required. The token itself is the credential.
    """
    now = datetime.now(timezone.utc)

    result = await db.execute(
        select(PortalToken).where(
            PortalToken.token == token,
            PortalToken.token_type == "lender",
            PortalToken.expires_at > now,
        )
    )
    portal_token = result.scalar_one_or_none()
    if portal_token is None:
        raise HTTPException(
            status_code=status.HTTP_404_NOT_FOUND,
            detail="Lender portal link is invalid or has expired.",
        )

    doc = Document(
        transaction_id=portal_token.transaction_id,
        name=body.name,
        phase=5,  # Financing phase
        responsible_party_role="lender",
    )
    db.add(doc)
    await db.flush()
    await db.refresh(doc)

    return {
        "id": doc.id,
        "transaction_id": doc.transaction_id,
        "name": doc.name,
        "status": doc.status,
        "created_at": doc.created_at.isoformat(),
    }
